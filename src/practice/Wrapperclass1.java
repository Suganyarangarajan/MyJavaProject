package practice;

public class Wrapperclass1 {

	public static void main(String[] args) {
		Integer inte = 234;
		Double doub = 23.56;
		Character cha = 'S';
		
		System.out.println(inte.intValue());
		System.out.println(doub.doubleValue());
		System.out.println(cha.charValue());

	}

}
